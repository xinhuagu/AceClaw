function addSessionNode(
  state: ExecutionTree,
  params: SessionStartedParams,
): ExecutionTree {
  // Idempotent: if the session node already exists (e.g. from a snapshot
  // replay), don't double-add. Compare on type+sessionId rather than node id
  // so we tolerate snapshot shapes that pre-date this dispatch.
  const existing = state.rootNodes.find(
    (n) => n.type === 'session' && n.id === params.sessionId,
  );
  if (existing) return state;

  const node: ExecutionNode = {
    id: params.sessionId,
    type: 'session',
    status: 'running',
    label: `session (${params.model})`,
    startTime: Date.parse(params.timestamp),
    children: [],
    metadata: { model: params.model },
  };
  return {
    ...state,
    rootNodes: [...state.rootNodes, node],
    activeNodeId: node.id,
  };
}

function closeSessionNode(
  state: ExecutionTree,
  params: SessionEndedParams,
): ExecutionTree {
  // No-op when the ended session is not the one this tree is rendering —
  // useExecutionTree's cross-session filter usually catches that, but the
  // reducer is single-session by contract anyway.
  if (params.sessionId !== state.sessionId) return state;
  const session = state.rootNodes.find(
    (n) => n.type === 'session' && n.id === params.sessionId,
  );
  if (!session) return state;
  // Mark the session node completed; preserve any in-flight child status
  // (the daemon already emitted turn_completed / tool_completed for those
  // before tearing the session down).
  return {
    ...state,
    rootNodes: mapNode(state.rootNodes, params.sessionId, (n) => ({
      ...n,
      status: 'completed' as const,
      endTime: Date.parse(params.timestamp),
      metadata: {
        ...(n.metadata ?? {}),
        endReason: params.reason,
      },
    })),
    activeNodeId: null,
  };
}

function addTurnNode(
  state: ExecutionTree,
  params: TurnStartedParams,
): ExecutionTree {
  // Turns nest under the deepest active step (when a plan is mid-execution)
  // or under the session otherwise. session_started may have been missed
  // (e.g. browser connected mid-prompt) — fall back to a root-level turn.
  const parent = findTurnParent(state, params.sessionId);

  const turn: ExecutionNode = {
    id: params.requestId,
    type: 'turn',
    status: 'running',
    label: `turn ${params.turnNumber}`,
    startTime: Date.parse(params.timestamp),
    children: [],
    metadata: { turnNumber: params.turnNumber },
  };

  let rootNodes: ExecutionNode[];
  if (parent) {
    rootNodes = appendChild(state.rootNodes, parent.id, turn);
  } else {
    // No session/step found — drop the turn at the root so it remains visible.
    rootNodes = [...state.rootNodes, turn];
  }

  return {
    ...state,
    rootNodes,
    activeNodeId: turn.id,
    // A new turn always starts a fresh ReAct loop — clear the thinking
    // and text anchors so the first event of this turn doesn't graft
    // itself onto the previous turn's last node.
    currentThinkingId: null,
    thinkingSealed: false,
    currentTextId: null,
    textIsOpen: false,
    stats: { ...state.stats, totalTurns: state.stats.totalTurns + 1 },
  };
}

function completeTurnNode(
  state: ExecutionTree,
  params: TurnCompletedParams,
): ExecutionTree {
  // Mark the turn AND every still-running thinking/text descendant as
  // completed. Tools manage their own status via stream.tool_completed,
  // but thinking and text are delta-only — there's no explicit "ended"
  // event for them, so without this sweep they'd stay in the 'running'
  // visual state (pulsing glow) forever even after the turn is over.
  // Capture stopReason on the turn's metadata so the renderer can
  // surface a badge for non-END_TURN endings (MAX_TOKENS, ERROR, …).
  // Older daemons that don't emit stopReason produce undefined, which
  // the renderer treats as "normal" — same visual as END_TURN.
  const rootNodes = mapNode(state.rootNodes, params.requestId, (n) => ({
    ...completeRunningDeltaDescendants(n),
    status: 'completed' as const,
    endTime: Date.parse(params.timestamp),
    duration: params.durationMs,
    metadata: {
      ...(n.metadata ?? {}),
      ...(params.stopReason !== undefined ? { stopReason: params.stopReason } : {}),
    },
  }));
  // Don't bubble activeNodeId up to the parent (session/step) when the
  // turn closes — that yanks the dashboard's auto-scroll back to the
  // session box, hiding the response the user just watched stream in.
  // Leaving activeNodeId on the last leaf makes the camera stay on the
  // response. The auto-scroll effect re-runs because layout.nodes
  // identity changed (turn status flipped to completed), but the
  // target is the leaf the user was already looking at, so the pan
  // is a no-op.
  return {
    ...state,
    rootNodes,
  };
}

/**
 * Returns {@code node} with every descendant of type {@code thinking} or
 * {@code text} that's still {@code running} flipped to {@code completed}.
 * Pure recursion; preserves all other fields. Used by
 * {@link completeTurnNode} so the turn's delta-stream children stop
 * pulsing the moment the turn closes.
 */
function completeRunningDeltaDescendants(node: ExecutionNode): ExecutionNode {
  if (node.children.length === 0) return node;
  const updated = node.children.map((c) => {
    const child = completeRunningDeltaDescendants(c);
    if ((child.type === 'thinking' || child.type === 'text') && child.status === 'running') {
      return { ...child, status: 'completed' as const };
    }
    return child;
  });
  return { ...node, children: updated };
}

function addToolNode(
  state: ExecutionTree,
  params: ToolUseParams,
): ExecutionTree {
  // Tools attach to the deepest causal node of the current iteration:
  //   1. The iteration's narration (text) when one exists — narration is
  //      the model verbalising its plan ("I'll call A and B"), and the
  //      tools that follow are the execution of that plan. Parent → child.
  //   2. Otherwise the iteration's thinking — when the model went straight
  //      from reasoning to acting with no narration, thinking is the cause.
  //   3. Otherwise the running turn — extended thinking off, or events
  //      arrived out of order. Fallback so the tool still surfaces.
  // Parallel tools from one LLM response don't get a thinking delta or
  // a text delta between them, so they share the same anchor — they
  // attach as siblings to whichever anchor was selected for the first
  // tool of the call.
  const turn = findNearestAncestor(
    state.rootNodes,
    state.activeNodeId,
    (n) => n.type === 'turn' && n.status === 'running',
  );
  // Tool anchor preference: the active narration text (if one is open
  // OR was the most recent under current thinking) > thinking > turn.
  // currentTextId stays valid through parallel tool_use events so they
  // share the same anchor. textIsOpen=false (after a tool closed the
  // text) keeps the anchor for parallel-tool grouping but signals to
  // the next text delta that it's a NEW iteration.
  const anchorId =
    state.currentTextId ?? state.currentThinkingId ?? (turn ? turn.id : null);
  const narrationId = state.currentTextId;

  const tool: ExecutionNode = {
    id: params.id,
    type: 'tool',
    status: 'running',
    label: params.summary ? `${params.name} — ${params.summary}` : params.name,
    children: [],
    metadata: { tool: params.name, summary: params.summary },
  };

  let rootNodes: ExecutionNode[];
  if (anchorId) {
    // Parallel detection: a running sibling tool under the same anchor (be
    // that the thinking or the turn) means this is a parallel call from
    // the same LLM response. One-way flag on the turn for sidebar stats.
    const anchorPath = findPath(state.rootNodes, anchorId);
    const anchorNode = anchorPath ? anchorPath[anchorPath.length - 1]! : null;
    const hasRunningSibling = anchorNode
      ? anchorNode.children.some(
          (c) => c.type === 'tool' && c.status === 'running',
        )
      : false;
    rootNodes = mapNode(state.rootNodes, anchorId, (a) => ({
      ...a,
      children: [...a.children, tool],
    }));
    if (turn && (hasRunningSibling || turn.parallel)) {
      // Conditional spread because exactOptionalPropertyTypes forbids
      // assigning `undefined` to an optional property.
      rootNodes = mapNode(rootNodes, turn.id, (t) => ({ ...t, parallel: true }));
    }
  } else {
    rootNodes = [...state.rootNodes, tool];
  }

  // Mark the thinking AND any narration text as completed: once
  // tool_use starts, the LLM has stopped both reasoning and talking
  // for this iteration — both should stop pulsing immediately so the
  // visual state matches reality. Only flip 'running' → 'completed'
  // so a node that already failed/etc. isn't clobbered.
  if (state.currentThinkingId) {
    rootNodes = mapNode(rootNodes, state.currentThinkingId, (t) =>
      t.status === 'running' ? { ...t, status: 'completed' as const } : t,
    );
  }
  if (narrationId) {
    rootNodes = mapNode(rootNodes, narrationId, (t) =>
      t.status === 'running' ? { ...t, status: 'completed' as const } : t,
    );
  }

  return {
    ...state,
    rootNodes,
    activeNodeId: tool.id,
    // Seal both anchors: a tool_use means the model has stopped
    // thinking AND stopped talking; the next thinking OR text delta is
    // from a NEW LLM call (next ReAct iteration). Parallel tool_use
    // events that follow within the SAME call don't have a thinking
    // or text delta between them, so currentThinkingId / currentTextId
    // stay valid for parallel-tool grouping.
    thinkingSealed: true,
    textIsOpen: false,
    stats: { ...state.stats, totalTools: state.stats.totalTools + 1 },
  };
}

function completeToolNode(
  state: ExecutionTree,
  params: ToolCompletedParams,
): ExecutionTree {
  const node = findNode(state.rootNodes, params.id);
  if (!node) return state;

  const status: ExecutionNode['status'] = params.isError ? 'failed' : 'completed';
  const rootNodes = mapNode(state.rootNodes, params.id, (n) => {
    const error = params.error ?? n.error;
    return {
      ...n,
      status,
      duration: params.durationMs,
      ...(error !== undefined ? { error } : {}),
    };
  });

  // Move active back up to the parent turn so the next tool_use attaches there.
  const path = findPath(rootNodes, params.id) ?? [];
  const parent = path.length >= 2 ? path[path.length - 2] : null;

  return {
    ...state,
    rootNodes,
    activeNodeId: parent ? parent.id : state.activeNodeId,
    stats: {
      ...state.stats,
      completedTools: state.stats.completedTools + (params.isError ? 0 : 1),
      failedTools: state.stats.failedTools + (params.isError ? 1 : 0),
    },
  };
}

/**
 * Truncates text for use as a node label. Tier-1 nodes are 180 px wide
 * and the renderer truncates to ~24 chars; we cap a bit shorter here
 * so the live preview reads cleanly while the text streams. Strips
 * leading whitespace because the model often opens a paragraph with a
 * newline and an indent that would otherwise dominate the preview.
 */
function previewLabel(text: string, max = 22): string {
  const trimmed = text.replace(/^\s+/, '');
  if (trimmed.length <= max) return trimmed;
  return `${trimmed.slice(0, max - 1)}…`;
}

function appendTextToCurrentTurn(
  state: ExecutionTree,
  params: TextDeltaParams,
): ExecutionTree {
  // Text in a Claude streaming response is per-LLM-call. Two anchors
  // separate "extend the live text" from "start a new iteration's
  // text": currentTextId tracks the live node; textIsOpen=false (set
  // by tool_use) signals that the text was sealed and the next delta
  // belongs to a NEW iteration. Without this flag, an iteration that
  // skips its thinking block (model went straight from tool result to
  // final response) would silently merge into the previous iter's
  // text node, because both ids would key on the same currentThinkingId.
  const turn = findNearestAncestor(
    state.rootNodes,
    state.activeNodeId,
    (n) => n.type === 'turn' && n.status === 'running',
  );
  if (!turn) return state;

  let textId: string;
  let isNewNode: boolean;
  let nextSyntheticId = state.nextSyntheticId;
  if (state.currentTextId && state.textIsOpen) {
    textId = state.currentTextId;
    isNewNode = false;
  } else {
    // New iteration: mint a unique id keyed on (anchor, counter).
    // The counter (nextSyntheticId) ensures uniqueness when an
    // iteration's text follows a tool_use without a fresh thinking
    // delta — both ids would key on the same currentThinkingId
    // otherwise and silently collide.
    const counter = nextSyntheticId;
    nextSyntheticId += 1;
    textId =
      state.currentThinkingId !== null
        ? `${state.currentThinkingId}:text:${counter}`
        : `${turn.id}:text:${counter}`;
    isNewNode = true;
  }

  const existing = !isNewNode ? findNode(state.rootNodes, textId) : null;
  const newText = (existing?.text ?? '') + params.delta;
  let rootNodes: ExecutionNode[];
  if (existing) {
    rootNodes = mapNode(state.rootNodes, textId, (t) => ({
      ...t,
      text: newText,
      label: previewLabel(newText),
    }));
  } else {
    const anchorId = state.currentThinkingId ?? turn.id;
    const textNode: ExecutionNode = {
      id: textId,
      type: 'text',
      status: 'running',
      label: previewLabel(newText),
      children: [],
      text: newText,
    };
    rootNodes = appendChild(state.rootNodes, anchorId, textNode);
  }

  // Mark the thinking anchor as completed: text means the LLM has
  // stopped thinking and is now talking.
  if (state.currentThinkingId) {
    rootNodes = mapNode(rootNodes, state.currentThinkingId, (t) =>
      t.status === 'running' ? { ...t, status: 'completed' as const } : t,
    );
  }

  // Move auto-scroll focus onto the streaming text. Subsequent deltas
  // keep the same id (textIsOpen stays true), so the camera sits on
  // this bubble until tool_use closes it or the turn ends.
  return {
    ...state,
    rootNodes,
    activeNodeId: textId,
    currentTextId: textId,
    textIsOpen: true,
    thinkingSealed: true,
    ...(isNewNode ? { nextSyntheticId } : {}),
  };
}

function appendThinkingToCurrentTurn(
  state: ExecutionTree,
  params: ThinkingDeltaParams,
): ExecutionTree {
  // Builds a {@code thinking}-typed node under the running turn. The
  // reducer used to drop these — meaning a turn that only thought (no
  // tools, no text yet) showed up as an empty parent. A turn can have
  // MULTIPLE thinking nodes, one per ReAct iteration — each iteration is a
  // separate LLM call, and the boundary is detected via {@link
  // ExecutionTree#thinkingSealed}: a tool_use or text delta seals the
  // current thinking node, so the next thinking delta starts a fresh one.
  // Parallel tool_use events from a single LLM response don't seal the
  // anchor between themselves (no thinking delta arrives between them), so
  // they correctly attach to the same parent.
  const turn = findNearestAncestor(
    state.rootNodes,
    state.activeNodeId,
    (n) => n.type === 'turn' && n.status === 'running',
  );
  if (!turn) return state;

  // Same iteration: append the delta to the existing thinking node.
  if (state.currentThinkingId && !state.thinkingSealed) {
    return {
      ...state,
      rootNodes: mapNode(state.rootNodes, state.currentThinkingId, (t) => ({
        ...t,
        text: (t.text ?? '') + params.delta,
      })),
    };
  }

  // New iteration boundary. The model has gathered the previous
  // iteration's tool results and is now reasoning about the next step
  // — by API definition, every tool from the previous iteration MUST
  // have completed (otherwise the model wouldn't have results to
  // reason about). Daemon's tool_completed events sometimes arrive
  // AFTER the next iteration's first thinking deltas (the daemon
  // kicks off the next LLM call before broadcasting completion), so
  // without reconciliation here the previous iteration's tools would
  // keep pulsing alongside the new thinking — visually looking
  // "parallel" when they're really sequential. Provisionally complete
  // the prior subtree's running tools/text now; when the real
  // tool_completed event arrives it'll overwrite with the correct
  // status (failed vs completed) plus duration/error metadata.
  let preparedRootNodes = state.rootNodes;
  if (state.currentThinkingId) {
    preparedRootNodes = mapNode(state.rootNodes, state.currentThinkingId, (n) =>
      provisionallyCompleteRunningWork(n),
    );
  }

  // Mint a new thinking node. Use a synthetic id keyed on the turn +
  // an index so multiple thinking nodes per turn each get a unique id
  // (the first uses the existing `${turnId}:thinking` id; later
  // iterations append a counter).
  const existingThinkingCount = countDescendantsOfType(turn, 'thinking');
  const id =
    existingThinkingCount === 0
      ? thinkingNodeId(turn.id)
      : `${thinkingNodeId(turn.id)}:${existingThinkingCount}`;
  const thinkingNode: ExecutionNode = {
    id,
    type: 'thinking',
    status: 'running',
    label: 'thinking',
    children: [],
    text: params.delta,
  };
  return {
    ...state,
    rootNodes: appendChild(preparedRootNodes, turn.id, thinkingNode),
    currentThinkingId: id,
    thinkingSealed: false,
    // New iteration: forget the previous iteration's text anchors so
    // tools/text in this iteration attach to the new thinking, not the
    // old text node.
    currentTextId: null,
    textIsOpen: false,
  };
}

/**
 * Returns {@code node} with every still-running {@code tool} or
 * {@code text} descendant flipped to {@code completed}. Used at
 * iteration boundaries to absorb the timing slop between a tool
 * finishing on the daemon side and its {@code tool_completed} event
 * arriving on the wire — the next iteration's events frequently
 * overtake the completion broadcast, leaving previous-iteration tools
 * stuck pulsing in the dashboard. The real tool_completed event is
 * still applied normally when it arrives; this just removes the
 * visual ambiguity in the gap.
 */
function provisionallyCompleteRunningWork(node: ExecutionNode): ExecutionNode {
  if (node.children.length === 0) return node;
  const updated = node.children.map((c) => {
    const child = provisionallyCompleteRunningWork(c);
    if (
      (child.type === 'tool' || child.type === 'text') &&
      child.status === 'running'
    ) {
      return { ...child, status: 'completed' as const };
    }
    return child;
  });
  return { ...node, children: updated };
}

/**
 * Counts every descendant of {@code root} (any depth) whose type matches.
 * Used to mint stable unique ids for repeated-per-turn synthetic nodes
 * (e.g. multiple thinking blocks across ReAct iterations).
 */
function countDescendantsOfType(
  root: ExecutionNode,
  type: ExecutionNode['type'],
): number {
  let count = 0;
  const stack: ExecutionNode[] = [...root.children];
  while (stack.length > 0) {
    const n = stack.pop()!;
    if (n.type === type) count += 1;
    if (n.children.length > 0) stack.push(...n.children);
  }
  return count;
}

function addPlanSkeleton(
  state: ExecutionTree,
  params: PlanCreatedParams,
): ExecutionTree {
  // Plan attaches to the nearest running turn (its conceptual owner). Steps
  // are pre-created as pending children so the dashboard can show the full
  // skeleton before any step runs — the headline visualisation feature.
  const owner =
    findNearestAncestor(
      state.rootNodes,
      state.activeNodeId,
      (n) => n.type === 'turn' && n.status === 'running',
    ) ??
    findNearestAncestor(
      state.rootNodes,
      state.activeNodeId,
      (n) => n.type === 'session',
    );

  const stepChildren: ExecutionNode[] = params.steps.map((s) => ({
    id: stepNodeId(params.planId, s.index),
    type: 'step',
    status: 'pending',
    label: s.name,
    children: [],
    metadata: { stepIndex: s.index, description: s.description },
  }));
  const plan: ExecutionNode = {
    id: params.planId,
    type: 'plan',
    status: 'running',
    label: `plan: ${params.goal}`,
    children: stepChildren,
    metadata: {
      planId: params.planId,
      stepCount: params.stepCount,
      resumed: params.resumed ?? false,
      ...(params.resumedFromStep !== undefined
        ? { resumedFromStep: params.resumedFromStep }
        : {}),
    },
  };

  if (owner) {
    return {
      ...state,
      rootNodes: appendChild(state.rootNodes, owner.id, plan),
    };
  }
  return { ...state, rootNodes: [...state.rootNodes, plan] };
}

function activateStep(
  state: ExecutionTree,
  params: PlanStepStartedParams,
): ExecutionTree {
  const composedId = stepNodeId(params.planId, params.stepIndex);
  const existing = findNode(state.rootNodes, composedId);

  // Skeleton-update path: the only state that means "this slot is a fresh
  // pre-created skeleton waiting to be activated" is 'pending'. Everything
  // else — running (duplicate event), completed/failed (replan after the
  // step already finished), cancelled (replan after pending), paused
  // (mid-permission) — is a tombstone whose history must be preserved. Mint
  // a synthetic id for the new step instead of overwriting.
  if (existing && existing.status === 'pending') {
    return {
      ...state,
      rootNodes: mapNode(state.rootNodes, composedId, (n) => ({
        ...n,
        status: 'running',
        label: params.stepName,
      })),
      activeNodeId: composedId,
    };
  }

  // From here, we either have no skeleton yet (out-of-order arrival) or we
  // hit a tombstone of any non-pending status left behind by replan. In both
  // cases we want to insert a NEW running step — but only if the plan itself
  // exists. If not, decline to move focus rather than dangle activeNodeId at
  // a node that no later handler can find.
  const plan = findNode(state.rootNodes, params.planId);
  if (!plan) {
    return state;
  }

  // Any existing tombstone (cancelled / completed / failed / running / paused)
  // must keep its history — a naive resurrection at the colliding composed id
  // would erase replan history or overwrite a successfully completed step's
  // record. Mint a synthetic id ({composedId}:r{n}) so the tombstone and the
  // new step coexist as siblings under the plan.
  const isReplanResurrection = existing != null;
  const newStepId = isReplanResurrection
    ? `${composedId}:r${state.nextSyntheticId}`
    : composedId;
  const newCounter = isReplanResurrection
    ? state.nextSyntheticId + 1
    : state.nextSyntheticId;

  const rootNodes = mapNode(state.rootNodes, params.planId, (p) => ({
    ...p,
    children: [
      ...p.children,
      {
        id: newStepId,
        type: 'step',
        status: 'running',
        label: params.stepName,
        children: [],
        metadata: {
          stepIndex: params.stepIndex,
          totalSteps: params.totalSteps,
          createdByReplan: isReplanResurrection,
        },
      },
    ],
  }));
  return {
    ...state,
    rootNodes,
    activeNodeId: newStepId,
    nextSyntheticId: newCounter,
  };
}

function completeStep(
  state: ExecutionTree,
  params: PlanStepCompletedParams,
): ExecutionTree {
  const composedId = stepNodeId(params.planId, params.stepIndex);
  // Resolve the step we should complete. Two paths:
  //   1. Composed id matches a still-running pre-created skeleton step.
  //   2. Plan has a running step at a synthetic id (replan path) — find it
  //      by scanning the plan's children rather than relying on activeNodeId,
  //      because the active node has typically moved to a child tool/text by
  //      the time plan_step_completed arrives.
  const composedExisting = findNode(state.rootNodes, composedId);
  const skeletonHit =
    composedExisting && composedExisting.status === 'running'
      ? composedId
      : null;
  const plan = findNode(state.rootNodes, params.planId);
  const replanRunningStep = plan?.children.find(
    (c) => c.type === 'step' && c.status === 'running',
  );
  const stepId = skeletonHit ?? replanRunningStep?.id ?? null;

  if (!stepId) {
    // No matching step (e.g. plan_step_completed before plan_created). Decline
    // to move focus — we can't honour the event meaningfully.
    return state;
  }

  const rootNodes = mapNode(state.rootNodes, stepId, (n) => ({
    ...n,
    status: params.success ? 'completed' : 'failed',
    duration: params.durationMs,
  }));
  // Step done → next pending sibling inherits focus, else the plan, else
  // leave focus alone (plan was missing too).
  const planAfter = findNode(rootNodes, params.planId);
  if (!planAfter) {
    return { ...state, rootNodes };
  }
  const nextPending = planAfter.children.find(
    (c) => c.type === 'step' && c.status === 'pending',
  );
  return {
    ...state,
    rootNodes,
    activeNodeId: nextPending ? nextPending.id : params.planId,
  };
}

function markStepFallback(
  state: ExecutionTree,
  params: PlanStepFallbackParams,
): ExecutionTree {
  const stepId = stepNodeId(params.planId, params.stepIndex);
  return {
    ...state,
    rootNodes: mapNode(state.rootNodes, stepId, (n) => ({
      ...n,
      metadata: {
        ...(n.metadata ?? {}),
        fallback: {
          approach: params.fallbackApproach,
          attempt: params.attempt,
        },
      },
    })),
  };
}

function replacePlanSteps(
  state: ExecutionTree,
  params: PlanReplannedParams,
): ExecutionTree {
  // The daemon's PlanReplanned only carries newStepCount + rationale, not the
  // full new step list (#439). Mark the plan with replan metadata so the
  // renderer can strike through the old step list; the next plan_step_started
  // events will land on synthesised step IDs that don't exist yet — handle
  // that by lazily creating them in activateStep when the lookup misses.
  return {
    ...state,
    rootNodes: mapNode(state.rootNodes, params.planId, (plan) => ({
      ...plan,
      children: plan.children.map((c) =>
        c.type === 'step' && c.status !== 'completed'
          ? { ...c, status: 'cancelled' as const }
          : c,
      ),
      metadata: {
        ...(plan.metadata ?? {}),
        replanAttempt: params.replanAttempt,
        replanRationale: params.rationale,
        newStepCount: params.newStepCount,
      },
    })),
  };
}

function completePlan(
  state: ExecutionTree,
  params: PlanCompletedParams,
): ExecutionTree {
  return {
    ...state,
    rootNodes: mapNode(state.rootNodes, params.planId, (n) => ({
      ...n,
      status: params.success ? 'completed' : 'failed',
      duration: params.totalDurationMs,
      metadata: {
        ...(n.metadata ?? {}),
        stepsCompleted: params.stepsCompleted,
        totalSteps: params.totalSteps,
      },
    })),
  };
}

function failPlan(
  state: ExecutionTree,
  params: PlanEscalatedParams,
): ExecutionTree {
  return {
    ...state,
    rootNodes: mapNode(state.rootNodes, params.planId, (n) => ({
      ...n,
      status: 'failed',
      error: params.reason,
    })),
  };
}

function addSubAgentNode(
  state: ExecutionTree,
  params: SubAgentStartParams,
): ExecutionTree {
  const turn = findNearestAncestor(
    state.rootNodes,
    state.activeNodeId,
    (n) => n.type === 'turn' && n.status === 'running',
  );
  // Sub-agent ids in the daemon are not unique across the same agentType
  // running twice in one turn (#439). Mint a synthetic id from the per-tree
  // monotonic counter so two subagent.start events back-to-back with the
  // same agentType cannot collide. The counter lives on state (not a module
  // variable) so the reducer stays pure and a snapshot replay produces the
  // same tree on every machine.
  const syntheticId = state.nextSyntheticId;
  const id = `subagent:${params.agentType}:${syntheticId}`;
  const node: ExecutionNode = {
    id,
    type: 'subagent',
    status: 'running',
    label: `subagent: ${params.agentType}`,
    children: [],
    metadata: { agentType: params.agentType, prompt: params.prompt },
  };
  const baseRootNodes = turn
    ? appendChild(state.rootNodes, turn.id, node)
    : [...state.rootNodes, node];
  return {
    ...state,
    rootNodes: baseRootNodes,
    activeNodeId: id,
    nextSyntheticId: syntheticId + 1,
  };
}

/**
 * Pattern that matches synthetic subagent ids minted by {@link addSubAgentNode},
 * format {@code subagent:{agentType}:{counter}}. Capture group 1 is the
 * monotonic counter that lets us pick "most recently started" deterministically
 * instead of relying on DFS traversal order.
 */
const SUBAGENT_ID_PATTERN = /^subagent:.+:(\d+)$/;

function completeSubAgentNode(
  state: ExecutionTree,
  params: SubAgentEndParams,
): ExecutionTree {
  // Daemon doesn't carry a unique id on subagent.end (#439), so we resolve
  // among running subagents matching the agentType. With two same-type
  // subagents alive in different branches, DFS order is whatever the tree
  // shape happens to be; using the highest synthetic counter gives
  // deterministic LIFO (last started, first ended), which is the
  // overwhelmingly common pattern. Snapshot-replay nodes that lack the
  // synthetic suffix fall back to the last DFS match.
  let target: ExecutionNode | null = null;
  let highestCounter = -1;
  const visit = (nodes: readonly ExecutionNode[]): void => {
    for (const n of nodes) {
      if (
        n.type === 'subagent' &&
        n.status === 'running' &&
        n.metadata?.['agentType'] === params.agentType
      ) {
        const m = SUBAGENT_ID_PATTERN.exec(n.id);
        if (m && m[1]) {
          const counter = Number(m[1]);
          if (counter > highestCounter) {
            highestCounter = counter;
            target = n;
          }
        } else if (highestCounter < 0) {
          // No synthetic suffix (e.g. snapshot-loaded node). Tracking here
          // only kicks in when no synthetic-id match has been found yet.
          target = n;
        }
      }
      visit(n.children);
    }
  };
  visit(state.rootNodes);
  if (!target) return state;
  const targetId = (target as ExecutionNode).id;
  return {
    ...state,
    rootNodes: mapNode(state.rootNodes, targetId, (n) => ({
      ...n,
      status: 'completed',
    })),
  };
}

function pauseForPermission(
  state: ExecutionTree,
  params: PermissionRequestParams,
): ExecutionTree {
  // The daemon pauses execution waiting for a permission.response. We mark
  // the active leaf as paused + awaitingInput so the renderer can surface
  // the prompt right where the agent stopped.
  if (!state.activeNodeId) return state;
  return {
    ...state,
    rootNodes: mapNode(state.rootNodes, state.activeNodeId, (n) => ({
      ...n,
      status: 'paused',
      awaitingInput: true,
      inputPrompt: params.description,
      permissionRequestId: params.requestId,
      metadata: { ...(n.metadata ?? {}), permissionTool: params.tool },
    })),
  };
}

function updateUsageStats(
  state: ExecutionTree,
  params: UsageParams,
): ExecutionTree {
  return {
    ...state,
    stats: {
      ...state.stats,
      // totalInputTokens / totalOutputTokens are cumulative on the daemon
      // side — mirror the latest snapshot rather than summing locally.
      inputTokens: params.totalInputTokens,
      outputTokens: params.totalOutputTokens,
    },
  };
}

function addCompactionMarker(
  state: ExecutionTree,
  params: CompactionParams,
): ExecutionTree {
  // Surface a compaction event on the active turn's metadata so the renderer
  // can draw a small marker. No new node — compaction is metadata, not a
  // first-class tree node.
  const turn = findNearestAncestor(
    state.rootNodes,
    state.activeNodeId,
    (n) => n.type === 'turn',
  );
  if (!turn) return state;
  return {
    ...state,
    rootNodes: mapNode(state.rootNodes, turn.id, (t) => {
      const prior = (t.metadata?.['compactions'] as unknown[]) ?? [];
      return {
        ...t,
        metadata: {
          ...(t.metadata ?? {}),
          compactions: [
            ...prior,
            {
              originalTokens: params.originalTokens,
              compactedTokens: params.compactedTokens,
              phase: params.phase,
            },
          ],
        },
      };
    }),
  };
}

