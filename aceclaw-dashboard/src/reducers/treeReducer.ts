/**
 * Tier 1 EventReducer (issue #435).
 *
 * Pure function {@code (state, envelope) → newState} that turns the daemon's
 * flat event stream (from the WebSocket bridge, #431) into a hierarchical
 * {@link ExecutionTree}. The reducer is the ONLY place where flat → tree
 * mapping happens — see #439 for why this lives in the frontend rather than
 * the daemon.
 *
 * Architectural rules
 * - Pure: no I/O, no time-dependent helpers (timestamps come from envelopes).
 * - Immutable: every update returns a new state; never mutates inputs.
 * - Forward-compatible: unknown methods are silently ignored so Tier 2/3
 *   additions don't require reducer changes.
 * - Watermark-aware: every dispatch advances {@code state.lastEventId} so
 *   #432's snapshot/reconnect can deduplicate cleanly.
 */

import type {
  DaemonEvent,
  DaemonEventEnvelope,
  PermissionRequestParams,
  PlanCreatedParams,
  PlanStepStartedParams,
  PlanStepCompletedParams,
  PlanStepFallbackParams,
  PlanReplannedParams,
  PlanCompletedParams,
  PlanEscalatedParams,
  SessionEndedParams,
  SessionStartedParams,
  SubAgentEndParams,
  SubAgentStartParams,
  TextDeltaParams,
  ThinkingDeltaParams,
  ToolCompletedParams,
  ToolUseParams,
  TurnCompletedParams,
  TurnStartedParams,
  UsageParams,
  CompactionParams,
} from '../types/events';
import type { ExecutionNode, ExecutionTree } from '../types/tree';

/**
 * The reducer takes only well-typed envelopes. Forward-compatibility for
 * Tier 2/3 events is enforced at the hook layer — the hook validates the
 * inbound method against the {@link DaemonEvent} union and drops anything
 * unknown before dispatch. That keeps the switch below exhaustive in the
 * type system without sprinkling casts through every case body.
 */
type Envelope = DaemonEventEnvelope<DaemonEvent>;

// ---------------------------------------------------------------------------
// ID helpers — single source of truth for synthetic node ids
// ---------------------------------------------------------------------------
//
// These ids are generated by the reducer (the daemon doesn't know about
// frontend node identity), so create-site and lookup-site MUST agree on the
// format. Centralising the format here prevents drift bugs that would
// otherwise be silent — a typo in one place would just no-op the lookup.

/** ID for the synthesised step node under a plan. 1-based to match the wire format. */
export function stepNodeId(planId: string, stepIndex: number): string {
  return `${planId}:step:${stepIndex}`;
}

/** ID for the rolled-up text-bubble child of a turn. One per turn. */
export function textNodeId(turnId: string): string {
  return `${turnId}:text`;
}

/** ID for the rolled-up thinking-bubble child of a turn. One per turn. */
export function thinkingNodeId(turnId: string): string {
  return `${turnId}:thinking`;
}

// ---------------------------------------------------------------------------
// Tree helpers (pure, recursive, immutable)
// ---------------------------------------------------------------------------

/** Returns the path from root to {@code id}, or null if not found. */
function findPath(
  nodes: ExecutionNode[],
  id: string,
): ExecutionNode[] | null {
  for (const n of nodes) {
    if (n.id === id) return [n];
    const sub = findPath(n.children, id);
    if (sub) return [n, ...sub];
  }
  return null;
}

/** Returns the matching node anywhere in the tree, or null. */
function findNode(
  nodes: ExecutionNode[],
  id: string,
): ExecutionNode | null {
  for (const n of nodes) {
    if (n.id === id) return n;
    const sub = findNode(n.children, id);
    if (sub) return sub;
  }
  return null;
}

/**
 * Returns the nearest ancestor of {@code fromId} (inclusive) for which
 * {@code predicate} holds, or null. Walks the path root→fromId in reverse so
 * "fromId itself" wins over its parent on a tie.
 */
function findNearestAncestor(
  rootNodes: ExecutionNode[],
  fromId: string | null,
  predicate: (n: ExecutionNode) => boolean,
): ExecutionNode | null {
  if (!fromId) return null;
  const path = findPath(rootNodes, fromId);
  if (!path) return null;
  for (let i = path.length - 1; i >= 0; i--) {
    const candidate = path[i];
    if (candidate && predicate(candidate)) return candidate;
  }
  return null;
}

/**
 * Replaces a node anywhere in the forest by id. Returns a new forest with
 * structural sharing for branches that did not change.
 */
function mapNode(
  nodes: ExecutionNode[],
  id: string,
  updater: (n: ExecutionNode) => ExecutionNode,
): ExecutionNode[] {
  let changed = false;
  const next = nodes.map((n) => {
    if (n.id === id) {
      changed = true;
      return updater(n);
    }
    if (n.children.length === 0) return n;
    const newChildren = mapNode(n.children, id, updater);
    if (newChildren !== n.children) {
      changed = true;
      return { ...n, children: newChildren };
    }
    return n;
  });
  return changed ? next : nodes;
}

/** Appends a child under {@code parentId}. Parent must already exist. */
function appendChild(
  nodes: ExecutionNode[],
  parentId: string,
  child: ExecutionNode,
): ExecutionNode[] {
  return mapNode(nodes, parentId, (parent) => ({
    ...parent,
    children: [...parent.children, child],
  }));
}

/**
 * Finds the natural parent for a new turn or plan: the deepest running step
 * (when a plan is mid-execution), else the deepest session ancestor of the
 * current active node, else any session matching {@code sessionId} at the
 * root, else null. Centralises the three-fallback walk that
 * {@link addTurnNode} and {@link addPlanSkeleton} both need.
 */
function findTurnParent(
  state: ExecutionTree,
  sessionId: string,
): ExecutionNode | null {
  return (
    findNearestAncestor(
      state.rootNodes,
      state.activeNodeId,
      (n) => n.type === 'step' && n.status === 'running',
    ) ??
    findNearestAncestor(
      state.rootNodes,
      state.activeNodeId,
      (n) => n.type === 'session',
    ) ??
    state.rootNodes.find((n) => n.id === sessionId) ??
    null
  );
}

